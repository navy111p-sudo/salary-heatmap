let currentLang = localStorage.getItem("mangoi_lang") || "ko";

function translateDOM(lang) {
  const walk = document.createTreeWalker(
    document.body,
    NodeFilter.SHOW_TEXT,
    null,
    false,
  );
  let node;
  while ((node = walk.nextNode())) {
    if (
      node.parentElement &&
      (node.parentElement.tagName === "SCRIPT" ||
        node.parentElement.tagName === "STYLE")
    )
      continue;

    let orig = node._i18nKey;
    if (!orig) {
      // 줄바꿈과 다중 공백을 하나의 공백으로 정규화
      let t = node.textContent.trim().replace(/\s+/g, " ");
      if (i18nDict[t]) {
        orig = t;
        node._i18nKey = orig;
      }
    }

    if (orig && i18nDict[orig] && i18nDict[orig][lang]) {
      const paddingMatch = node.textContent.match(/^(\s*)(.*?)(\s*)$/);
      if (paddingMatch) {
        node.textContent =
          paddingMatch[1] + i18nDict[orig][lang] + paddingMatch[3];
      } else {
        node.textContent = i18nDict[orig][lang];
      }
    }
  }

  // Set dropdown label
  const labelMap = { ko: "KOR", en: "ENG", tl: "TAG" };
  const labelNode = document.getElementById("currentLangLabel");
  if (labelNode) labelNode.textContent = labelMap[lang];

  // Update report dynamic i18n
  if (typeof updateReportI18n === "function") updateReportI18n();
}

document.addEventListener("DOMContentLoaded", () => {
  // Initial translation
  translateDOM(currentLang);

  // Dropdown event listeners
  const btn = document.getElementById("langSelectBtn");
  const menu = document.getElementById("langDropdownMenu");

  if (btn && menu) {
    btn.addEventListener("click", (e) => {
      e.stopPropagation();
      menu.classList.toggle("hidden");
    });

    document.documentElement.addEventListener("click", () => {
      if (!menu.classList.contains("hidden")) {
        menu.classList.add("hidden");
      }
    });

    document.querySelectorAll(".lang-option").forEach((item) => {
      item.addEventListener("click", (e) => {
        const lang = e.target.getAttribute("data-lang");
        currentLang = lang;
        localStorage.setItem("mangoi_lang", lang);
        translateDOM(lang);
        // 히트맵 범례 다국어 재렌더링
        if (typeof renderGridHeatmap === "function") renderGridHeatmap();
        // 계산기 툴팁 다국어 재렌더링
        if (typeof renderTooltip === "function") renderTooltip();
      });
    });
  }

  // Observe dynamically added elements (like report tables, heatmap tiles)
  let isTranslating = false;
  const observer = new MutationObserver((mutations) => {
    if (isTranslating) return;
    let shouldTranslate = false;
    mutations.forEach((m) => {
      if (m.addedNodes.length > 0) shouldTranslate = true;
    });
    // Use requestAnimationFrame for debouncing inside observer
    if (shouldTranslate) {
      isTranslating = true;
      requestAnimationFrame(() => {
        translateDOM(currentLang);
        isTranslating = false;
      });
    }
  });

  observer.observe(document.body, { childList: true, subtree: true });
  });
